#set methods
a={1,2,3,4}
b={5,6,7,8}
print(a.union(b))
print(a.intersection(b))
print(a|b)
print(a&b)
print(a-b)
print(b-a)
print(a.difference(b))
print(a.difference_update(b))
print(a^b)
print(a.symmetric_difference_update(b))
print(a.isdisjoint(b))
print(a.issuperset(b))
print(a.issubset(b))
